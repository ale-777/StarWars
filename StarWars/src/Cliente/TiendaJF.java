    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        String nombreC = lblNombre.getText();
        if (nombreC == "Armeria"){
            nombreC += " "+cbbArmeria.getSelectedItem();
        }
        int costo = parseInt(lblCosto.getText());
        if (parseInt(lblDinero.getText()) >= costo){
            try {
                refPantalla.comproComp(nombreC,costo);
            } catch (IOException ex) {
                Logger.getLogger(TiendaJF.class.getName()).log(Level.SEVERE, null, ex);
            }
            cbbArmeria.setSelectedIndex(-1);
        }
        else noTieneSuficienteDinero();
    }//GEN-LAST:event_btnPagarActionPerformed

