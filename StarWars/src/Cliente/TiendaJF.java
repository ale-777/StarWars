    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int index = cbbComponentes.getSelectedIndex();
        int x = (int)spnX.getValue();
        int y = (int)spnY.getValue();
        String nombre = (String) cbbComponentes.getSelectedItem();
        String orientacion = (String) cbbOrientacion.getSelectedItem();
        ArrayList<String> datos = new ArrayList<>();
        datos.add(nombre);datos.add(x+"");datos.add(y+"");datos.add(orientacion);
        if(index > -1 && refPantalla.casillaValida(nombre,x,y,orientacion)){
            //refPantalla.componentesDisponibles.remove(index);
            try {
                refPantalla.colocarComponente(datos);
            } catch (IOException ex) {
                Logger.getLogger(TiendaJF.class.getName()).log(Level.SEVERE, null, ex);
            }
            updateCBB();
        }
        else{
            JOptionPane.showMessageDialog(this, "Datos invalidos");
        }
        
        
    }//GEN-LAST:event_jButton8ActionPerformed

